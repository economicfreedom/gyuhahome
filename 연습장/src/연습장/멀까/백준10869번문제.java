package 연습장.멀까;
import java.util.Scanner;

public class 백준10869번문제 {

	public static void main(String[] args) {
		
		Scanner v = new Scanner(System.in);
		int A = v.nextInt();
		int B = v.nextInt();
		
		System.out.println(A+B);
		System.out.println(A-B);
		System.out.println(A*B);
		System.out.println((double)A/(double)B);
		System.out.println((double)A%(double)B);
	}

}
